package Statements;

public class StatementsConcepts {
    public static void main(String[] args) {
        // 1. if statement : Use only when you know what to do when condition is true
        // 2. if else statement : use when both boolean condition can be handled
        // 3. if else if statement
        // 4. switch statements

        int x= 6;
        if(x>7)
        {
            System.out.println( x + " is greater than 7");
        }
        if(x>2)
        {
            System.out.println( x + " is greater than 7");
        }
        if(x>3)
        {
            System.out.println( x + " is greater than 7");
        }
        else
        {
            System.out.println(x + " is smaller than 7");
        }

    }
}
